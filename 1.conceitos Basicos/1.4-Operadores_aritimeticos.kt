fun main() {
    
   //operadores aritimeticos:

    var n1 = 24;
    var n2 = 2;
    
    n1 ++;
    println("Incrementação: $n1");

    n1 --;
    println("Decrementação: $n1");

    n1 *= 2;
    println("Multiplicação: $n1");

    n1 /= 2;
    println("Divisão: $n1");

    n1 % n2 ;
    println("modulo: $n1");
    
}
